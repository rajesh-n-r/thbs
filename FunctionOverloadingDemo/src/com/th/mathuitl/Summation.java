package com.th.mathuitl;

public class Summation {
	
	public static int add(int x, int y)
	{
		return(x+y);
	}
	
	public static double add(int x, long y,int z)
	{
		return(x+y+z);
	}
	
	public static double add(float x, int y)
	{
		return(x+y);
	}
	
	
	public static float add(float x,double  y)
	{
		return(float)(x+y);
	}
	
	
	public static  String add(String s1, String s2)
	{
		return(s1+"  "+s2);
	}

}
